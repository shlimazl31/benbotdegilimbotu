import { SlashCommandBuilder, EmbedBuilder, ActionRowBuilder, ButtonBuilder, ButtonStyle } from 'discord.js';
import { getPlayer } from '../../utils/player.js';

// Her sunucu i√ßin son nowplaying mesajƒ±nƒ± tutacak Map
const lastNowPlayingMessages = new Map();

function createProgressBar(progress) {
    if (!progress || isNaN(progress) || progress < 0 || progress > 1) {
        progress = 0;
    }
    
    const length = 12;
    const filled = Math.max(0, Math.min(length, Math.round(length * progress)));
    const empty = length - filled;
    
    const filledBar = '‚ñ¨'.repeat(filled);
    const emptyBar = '‚ñ¨'.repeat(empty);
    
    return `${filledBar}üîò${emptyBar}`;
}

function formatTime(ms) {
    if (!ms || isNaN(ms)) return '00:00';
    
    const totalSeconds = Math.floor(ms / 1000);
    const minutes = Math.floor(totalSeconds / 60);
    const seconds = totalSeconds % 60;
    
    return `${minutes.toString().padStart(2, '0')}:${seconds.toString().padStart(2, '0')}`;
}

export const command = {
    data: new SlashCommandBuilder()
        .setName('nowplaying')
        .setDescription('≈ûu anda √ßalan ≈üarkƒ±yƒ± g√∂sterir'),

    async execute(interaction) {
        try {
            // √ñnceki nowplaying mesajƒ±nƒ± sil
            const lastMsg = lastNowPlayingMessages.get(interaction.guildId);
            if (lastMsg) {
                try { await lastMsg.delete().catch(() => {}); } catch {}
            }

            const player = await getPlayer(interaction.client);
            const queue = player.nodes.get(interaction.guildId);

            if (!queue || !queue.isPlaying()) {
                return await interaction.reply({
                    content: '‚ùå ≈ûu anda √ßalan bir ≈üarkƒ± yok!',
                    ephemeral: true
                });
            }

            const track = queue.currentTrack;
            if (!track) {
                return await interaction.reply({
                    content: '‚ùå ≈ûu anda √ßalan bir ≈üarkƒ± yok!',
                    ephemeral: true
                });
            }

            // ≈ûarkƒ± t√ºr√ºne g√∂re renk belirleme
            let color = '#FF0000'; // Varsayƒ±lan renk
            const title = track.title.toLowerCase();
            
            if (title.includes('pop')) color = '#FF69B4';
            else if (title.includes('rock')) color = '#FF0000';
            else if (title.includes('rap') || title.includes('hip hop')) color = '#000000';
            else if (title.includes('classic')) color = '#FFD700';
            else if (title.includes('jazz')) color = '#8B4513';
            else if (title.includes('electronic') || title.includes('edm')) color = '#00FFFF';

            const progress = queue.node.getTimestamp();
            const progressPercent = progress?.current && track?.durationMS 
                ? progress.current / track.durationMS 
                : 0;

            const currentTime = formatTime(progress?.current || 0);
            const totalTime = formatTime(track?.durationMS || 0);
            const progressBar = createProgressBar(progressPercent);

            const embed = new EmbedBuilder()
                .setTitle('üéµ ≈ûimdi √áalƒ±yor')
                .setDescription(`**${track.title}**`)
                .addFields(
                    { name: 'üë§ Sanat√ßƒ±', value: track.author || 'Bilinmiyor', inline: true },
                    { name: '‚è±Ô∏è S√ºre', value: totalTime, inline: true },
                    { name: 'üîä Ses', value: `${queue.node.volume}%`, inline: true },
                    { name: 'üìä ƒ∞lerleme', value: `${currentTime} ‚îÉ ${progressBar} ‚îÉ ${totalTime}`, inline: false }
                )
                .setThumbnail(track.thumbnail)
                .setColor(color)
                .setFooter({ 
                    text: `ƒ∞steyen: ${track.requestedBy?.tag || interaction.user.tag}`,
                    iconURL: track.requestedBy?.displayAvatarURL() || interaction.user.displayAvatarURL()
                });

            // Kontrol butonlarƒ±
            const row = new ActionRowBuilder()
                .addComponents(
                    new ButtonBuilder()
                        .setCustomId('pause')
                        .setLabel(queue.node.isPaused() ? '‚ñ∂Ô∏è Devam Et' : '‚è∏Ô∏è Duraklat')
                        .setStyle(ButtonStyle.Primary),
                    new ButtonBuilder()
                        .setCustomId('skip')
                        .setLabel('‚è≠Ô∏è Ge√ß')
                        .setStyle(ButtonStyle.Secondary),
                    new ButtonBuilder()
                        .setCustomId('loop')
                        .setLabel(queue.repeatMode === 0 ? 'üîÅ Tekrarla' : 'üîÅ Tekrarƒ± Kapat')
                        .setStyle(queue.repeatMode === 0 ? ButtonStyle.Secondary : ButtonStyle.Success),
                    new ButtonBuilder()
                        .setCustomId('shuffle')
                        .setLabel('üîÄ Karƒ±≈ütƒ±r')
                        .setStyle(ButtonStyle.Secondary),
                    new ButtonBuilder()
                        .setCustomId('stop')
                        .setLabel('‚èπÔ∏è Durdur')
                        .setStyle(ButtonStyle.Danger)
                );

            const message = await interaction.reply({
                embeds: [embed],
                components: [row],
                fetchReply: true
            });

            // Son mesajƒ± kaydet
            lastNowPlayingMessages.set(interaction.guildId, message);

            // Buton etkile≈üimlerini topla
            const collector = message.createMessageComponentCollector({
                time: 300000 // 5 dakika
            });

            collector.on('collect', async (i) => {
                if (i.user.id !== interaction.user.id) {
                    return await i.reply({
                        content: '‚ùå Bu butonlarƒ± sadece komutu kullanan ki≈üi kullanabilir!',
                        ephemeral: true
                    });
                }

                const queue = player.nodes.get(i.guildId);
                if (!queue) {
                    return await i.reply({
                        content: '‚ùå ≈ûu anda √ßalan bir ≈üarkƒ± yok!',
                        ephemeral: true
                    });
                }

                switch (i.customId) {
                    case 'pause':
                        queue.node.setPaused(!queue.node.isPaused());
                        await i.update({
                            components: [
                                new ActionRowBuilder()
                                    .addComponents(
                                        new ButtonBuilder()
                                            .setCustomId('pause')
                                            .setLabel(queue.node.isPaused() ? '‚ñ∂Ô∏è Devam Et' : '‚è∏Ô∏è Duraklat')
                                            .setStyle(ButtonStyle.Primary),
                                        new ButtonBuilder()
                                            .setCustomId('skip')
                                            .setLabel('‚è≠Ô∏è Ge√ß')
                                            .setStyle(ButtonStyle.Secondary),
                                        new ButtonBuilder()
                                            .setCustomId('loop')
                                            .setLabel(queue.repeatMode === 0 ? 'üîÅ Tekrarla' : 'üîÅ Tekrarƒ± Kapat')
                                            .setStyle(queue.repeatMode === 0 ? ButtonStyle.Secondary : ButtonStyle.Success),
                                        new ButtonBuilder()
                                            .setCustomId('shuffle')
                                            .setLabel('üîÄ Karƒ±≈ütƒ±r')
                                            .setStyle(ButtonStyle.Secondary),
                                        new ButtonBuilder()
                                            .setCustomId('stop')
                                            .setLabel('‚èπÔ∏è Durdur')
                                            .setStyle(ButtonStyle.Danger)
                                    )
                            ]
                        });
                        break;

                    case 'skip':
                        queue.node.skip();
                        await i.update({
                            components: []
                        });
                        break;

                    case 'loop':
                        queue.setRepeatMode(queue.repeatMode === 0 ? 1 : 0);
                        await i.update({
                            components: [
                                new ActionRowBuilder()
                                    .addComponents(
                                        new ButtonBuilder()
                                            .setCustomId('pause')
                                            .setLabel(queue.node.isPaused() ? '‚ñ∂Ô∏è Devam Et' : '‚è∏Ô∏è Duraklat')
                                            .setStyle(ButtonStyle.Primary),
                                        new ButtonBuilder()
                                            .setCustomId('skip')
                                            .setLabel('‚è≠Ô∏è Ge√ß')
                                            .setStyle(ButtonStyle.Secondary),
                                        new ButtonBuilder()
                                            .setCustomId('loop')
                                            .setLabel(queue.repeatMode === 0 ? 'üîÅ Tekrarla' : 'üîÅ Tekrarƒ± Kapat')
                                            .setStyle(queue.repeatMode === 0 ? ButtonStyle.Secondary : ButtonStyle.Success),
                                        new ButtonBuilder()
                                            .setCustomId('shuffle')
                                            .setLabel('üîÄ Karƒ±≈ütƒ±r')
                                            .setStyle(ButtonStyle.Secondary),
                                        new ButtonBuilder()
                                            .setCustomId('stop')
                                            .setLabel('‚èπÔ∏è Durdur')
                                            .setStyle(ButtonStyle.Danger)
                                    )
                            ]
                        });
                        break;

                    case 'shuffle':
                        queue.tracks.shuffle();
                        await i.update({
                            components: []
                        });
                        break;

                    case 'stop':
                        queue.delete();
                        await i.update({
                            components: []
                        });
                        break;
                }
            });

            collector.on('end', () => {
                message.edit({
                    components: []
                }).catch(() => {});
            });

            // Her 10 saniyede bir mesajƒ± g√ºncelle
            const updateInterval = setInterval(async () => {
                try {
                    const currentQueue = player.nodes.get(interaction.guildId);
                    if (!currentQueue || !currentQueue.isPlaying()) {
                        clearInterval(updateInterval);
                        return;
                    }

                    const currentTrack = currentQueue.currentTrack;
                    if (!currentTrack) {
                        clearInterval(updateInterval);
                        return;
                    }

                    const progress = currentQueue.node.getTimestamp();
                    const progressPercent = progress?.current && currentTrack?.durationMS 
                        ? progress.current / currentTrack.durationMS 
                        : 0;

                    const currentTime = formatTime(progress?.current || 0);
                    const totalTime = formatTime(currentTrack?.durationMS || 0);
                    const progressBar = createProgressBar(progressPercent);

                    const updatedEmbed = new EmbedBuilder()
                        .setTitle('üéµ ≈ûimdi √áalƒ±yor')
                        .setDescription(`**${currentTrack.title}**`)
                        .addFields(
                            { name: 'üë§ Sanat√ßƒ±', value: currentTrack.author || 'Bilinmiyor', inline: true },
                            { name: '‚è±Ô∏è S√ºre', value: totalTime, inline: true },
                            { name: 'üîä Ses', value: `${currentQueue.node.volume}%`, inline: true },
                            { name: 'üìä ƒ∞lerleme', value: `${currentTime} ‚îÉ ${progressBar} ‚îÉ ${totalTime}`, inline: false }
                        )
                        .setThumbnail(currentTrack.thumbnail)
                        .setColor(color)
                        .setFooter({ 
                            text: `ƒ∞steyen: ${currentTrack.requestedBy?.tag || interaction.user.tag}`,
                            iconURL: currentTrack.requestedBy?.displayAvatarURL() || interaction.user.displayAvatarURL()
                        });

                    await message.edit({
                        embeds: [updatedEmbed]
                    });
                } catch (error) {
                    console.error('Mesaj g√ºncelleme hatasƒ±:', error);
                    clearInterval(updateInterval);
                }
            }, 10000);

            // 15 dakika sonra g√ºncellemeyi durdur
            setTimeout(() => {
                clearInterval(updateInterval);
            }, 15 * 60 * 1000);

        } catch (error) {
            console.error('Nowplaying hatasƒ±:', error);
            if (!interaction.replied && !interaction.deferred) {
                await interaction.reply({
                    content: '‚ùå Bir hata olu≈ütu!',
                    ephemeral: true
                });
            }
        }
    }
}; 